.MACRO  SETOUT
	LDI @0, @1
	OUT @2, @0
.ENDMACRO

.MACRO TDELAY
	LDI R20,@0
L1: 	LDI R21,@1
L2: 	LDI R22,@2
L3: 	NOP
	NOP
	DEC R22
	BRNE L3
	DEC R21
	BRNE L2
	DEC R20
	BRNE L1
.ENDMACRO

